using UnityEngine;

//this class performs operations on bezier curves, is adapted from http://catlikecoding.com/unity/tutorials/curves-and-splines/
public static class Bezier {

	//we use cubic bezier curve formulae based on linear interpolations to find the local position of the point
	//that corresponds to t progress (0-beginning, 1-end) along the curve, given 4 control points
	public static Vector3 GetPoint (Vector3 p0, Vector3 p1, Vector3 p2, Vector3 p3, float t) {
		t = Mathf.Clamp01(t);
		float OneMinusT = 1f - t;
		return
			OneMinusT * OneMinusT * OneMinusT * p0 +
			3f * OneMinusT * OneMinusT * t * p1 +
			3f * OneMinusT * t * t * p2 +
			t * t * t * p3;
	}


	//used to calculate "velocity" of the curve
	public static Vector3 GetFirstDerivative (Vector3 p0, Vector3 p1, Vector3 p2, Vector3 p3, float t) {
		t = Mathf.Clamp01(t);
		float oneMinusT = 1f - t;
		return
			3f * oneMinusT * oneMinusT * (p1 - p0) +
			6f * oneMinusT * t * (p2 - p1) +
			3f * t * t * (p3 - p2);
	}
}